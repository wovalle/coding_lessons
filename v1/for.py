# Escriba un programa que me devuelva la suma hasta n

# Usuario ingresa 5 (1,2,3,4,5)
# Imprime: Suma: 15

num = int(input('Ingrese n: ')) # 5
result = 0

for i in range(num + 1):
  result = result + i

print('La sumatoria hasta n de ', num, ' es igual a: ', result)


